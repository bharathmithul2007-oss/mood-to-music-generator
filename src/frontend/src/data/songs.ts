function buildDuration(id: number, lang: LangKey): string {
  const arr = DURATIONS[lang];
  const [m, s] = arr[id % arr.length];
  return `${m}:${String(s).padStart(2, "0")}`;
}

function buildSongs(
  raw: RawSong[],
  lang: LangKey,
  startId: number,
): LocalSong[] {
  return raw.map(([title, artist, album, year, genre, moods], i) => {
    const id = startId + i;
    return {
      id,
      title,
      artist,
      album,
      year,
      language: lang,
      genre,
      duration: buildDuration(id, lang),
      moods,
      streamUrl: S[id % 17],
      youtubeUrl: ytUrl(title, artist),
    };
  });
}

