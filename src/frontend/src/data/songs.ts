// ─────────────────────────────────────────────────────────
// RECOMMENDATION LOGIC
// Step 1: Filter by mood
// Step 2: Prioritize 2018-2026 > 2015-2017 > 2000-2014
// Step 3: Tamil first, English second, then others
// Step 4: Mix modern + older, avoid repetition
// ─────────────────────────────────────────────────────────
const LANG_PRIORITY: Record<string, number> = {
  Tamil: 0,
  English: 1,
  Hindi: 2,
  Telugu: 3,
  Malayalam: 4,
  Kannada: 5,
};

function tierOf(year: number): number {
  if (year >= 2018) return 0; // highest priority
  if (year >= 2015) return 1;
  return 2;
}

function seededShuffle<T>(arr: T[], seed: number): T[] {
  const a = [...arr];
  let s = seed;
  for (let i = a.length - 1; i > 0; i--) {
    s = (s * 1664525 + 1013904223) & 0xffffffff;
    const j = Math.abs(s) % (i + 1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function songsByMood(mood: string, seed?: number): LocalSong[] {
  const matching = songs.filter((s) => s.moods.includes(mood));

  // Sort: tier first, then language priority
  const sorted = [...matching].sort((a, b) => {
    const tierDiff = tierOf(a.year) - tierOf(b.year);
    if (tierDiff !== 0) return tierDiff;
    const langDiff =
      (LANG_PRIORITY[a.language] ?? 9) - (LANG_PRIORITY[b.language] ?? 9);
    return langDiff;
  });

  // Within each tier-language group, shuffle for variety
  const effectiveSeed =
    seed ?? mood.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
  const shuffled = seededShuffle(sorted, effectiveSeed);

  return shuffled;
}
